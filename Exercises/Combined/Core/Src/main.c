/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
	
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM21_Init();
  MX_ADC_Init();
  MX_I2C1_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */
	HAL_TIM_Base_Start_IT(&htim21);
	
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);
	
	HAL_ADCEx_Calibration_Start(&hadc,ADC_SINGLE_ENDED); 
	HAL_ADC_Start(&hadc);
	
	Initialize_LCD(hi2c1);
	clear_LCD();
	
	TimerCnt_Lcd   = 0;
	TimerCnt_Scan	 = 0;
	TimerCnt_Adc   = 0;
	ScanLine       = 0;
	voltage        = 0;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		if (TimerCnt_Adc == 0) {
			TimerCnt_Adc = 500;
			while(HAL_ADC_PollForConversion(&hadc,0) != HAL_OK);
			adc_v = HAL_ADC_GetValue(&hadc);
			
			pwm_duty = ((adc_v/4096.0)*100);
			pwm_duty =100-pwm_duty;
			voltage = (adc_v/4096.0)*3.3;
		}
		
		
		
		
		switch (mode) {
      case MODE_1:
			if (TimerCnt_Lcd == 0) {
			
				TimerCnt_Lcd = 2000;
				
				char str1[20];
				char str2[20];
				
				sprintf(str1, "Duty Cycle: %.3d", pwm_duty);  
				sprintf(str2, "Voltage: %.2f", voltage);  
				
				writes(0x80,str1);
				writes(0xC0,str2);
				
		  
			}
			
        break;
      case MODE_2:
				if (TimerCnt_Scan == 0) {
				TimerCnt_Scan = 10;
			
				GPIOC->BRR = 0xFFF;  
				__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1,7);
					switch (ScanLine) {
							case 0:
									Setpwm_duty(1,pwm_duty);//com1 on
									GPIOC ->BSRR =  TBL_7SEG[pwm_duty/1000]&0xff;
									ScanLine = 1;
									break;
							case 1:
									Setpwm_duty(2,pwm_duty);//com2 on
									GPIOC ->BSRR =  TBL_7SEG[(pwm_duty/100)%10]&0xff;
									ScanLine = 2;
									break;
							case 2:
									Setpwm_duty(3,pwm_duty);//com3 on
									GPIOC ->BSRR =  TBL_7SEG[(pwm_duty/10)%10]&0xff;
							
									ScanLine = 3;
									break;
							case 3:
									Setpwm_duty(4,pwm_duty);//com4 on
									GPIOC ->BSRR =  TBL_7SEG[pwm_duty%10]&0xff;

									ScanLine = 0;
									break;
					}
				}
			
        break;
    }
		

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

